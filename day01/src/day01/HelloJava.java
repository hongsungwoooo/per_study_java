package day01;

// class(클래스)는 자바 프로그램의 최소 단위 = {...} 클래스의 범위를 지정한다.
public class HelloJava {

	// main : 프로그램의 시작점을 지정하는 블록
	public static void main(String[] args) {
		// 괄호 안의 내용을 콘솔창에 출력하는 명령어
		// 블록안에 명시되는 명령어는 세미콜론(;)으로 문장의 끝을 표현한다.
		System.out.println("안녕하세요!!!");
		System.out.println();
	}

}