package value;

public class Main01 {

	public static void main(String[] args) {
		// 변수의 선언
		int num1;
		// 변수의 할당
		num1 = 100;
		
		System.out.println(num1);
		
		// 변수의 선언과 할당
		int num2 = 200;
				
		System.out.println(num2);

		System.out.println("-----------------------------");

		int age = 20;
		String name = "자바학생";
		String result = name + age;
		System.out.println(result);
		
		System.out.println("-----------------------------");
		
		// 선언 + 할당 + 접미사의 사용
		long num3 = 12345678900000L;
		double num4 = 128.4D;
		float num5 = 3.14F;
		
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		
	}

}
