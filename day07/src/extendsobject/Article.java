package extendsobject;

public class Article {				// 부모 클래스

	private int num;				// 글 번호
	private String title;			// 글 제목

	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
